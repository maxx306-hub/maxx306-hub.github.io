const read__more4 = document.querySelector('.show__more4');
const list4 = document.querySelector('.hidden__games4');

read__more4.addEventListener("click",(e)=>{
  list4.classList.toggle("ddd4");
  read__more4.classList.toggle("activee4");
  if(read__more4.innerText === "Show more"){
    read__more4.innerText = "Show less";
  }else {
    read__more4.innerText = "Show more";
  }
});


